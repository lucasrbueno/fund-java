//Faça um algoritmo que leia um número e mostre se o mesmo é par ou ímpar.

public class Ex12 {
    public static void main(String[] args) {
        int n = 96;
    
        if(n % 2 == 0){
            System.out.println("Número par");
        } else{
            System.out.println("Número ímpar");
        }
    }
}
